CREATE TABLE KHACHHANG (
MAKH CHAR(4) PRIMARY KEY,
HOTEN VARCHAR(40) NOT NULL,
DCHI VARCHAR(50),
SODT VARCHAR(20) NOT NULL,
NGSINH SMALLDATETIME,
DOANHSO MONEY,
NGDK SMALLDATETIME
